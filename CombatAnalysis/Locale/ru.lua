	if (initiatorName ~= nil) then
		local skillName = "Отражение";
		amount = string.gsub(amount,",","")+0;
		
		local dmgType = string.match(dmgType,"^урона(.*)$");
		-- a damage reflect
		if (dmgType ~= nil) then
			dmgType = 
				dmgType == " обычного типа" and 1 or
				dmgType == " огнем" and 2 or
				dmgType == " молнией" and 3 or
				dmgType == " холодом" and 4 or
				dmgType == " кислотой" and 5 or
				dmgType == " Тьмой" and 6 or
				dmgType == " Светом" and 7 or
				dmgType == " белериандского типа" and 8 or
