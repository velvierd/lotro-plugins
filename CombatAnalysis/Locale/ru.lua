				string.match(avoidAndCrit,"^критический") and 2 or
				string.match(avoidAndCrit,"^разгромный") and 3 or 1;
			amount,dmgType,moralePower = string.match(targetNameAmountAndType,"^([%d,]*) ед%.(.*)%. (.-) снижается%.$");
      dmgType = string.match(dmgType, "^(.*) %(.*%)$") or dmgType; -- 4.2.3 adjust for mounted combat
		else
