 Итак, вы вошли. Слева и справа от вас - проходы, путь куда вам преграждают решетки. Для того, чтобы они открылись, вам нужно активировать босса, который ждет вас внизу лестницы на площадке с огромной статуей. Спуститесь ему навстречу на пару ступенек, и он, завидев вас, закричит нечто устрашающе-шипящие и убежит в комнату, что расположена в дальнем конце площадки. В этот момент статуи, что стоят по кругу в выемках на балконе, начнут разворачиваться, переставая отражать свет кристалла, что висит под потолком. 
 Внимание. 
 Важно запомнить порядок, в котором статуи будут крутиться - после зачистки всего балкона вам придется разворачивать их в исходное положение. Причем порядок будет обратный: т.е. та статуя, что развернулась последней, должна быть обращена к кристаллу первой. 
 По сути, если смотреть от входа, статуи расположены так: 

 ---------------------- 
 ---3л-----3пр------- 
 2л------------2пр--- 
 ---1л-----1пр------- 
 -------вы------------- 

 Итого, если после криков босса, статуи крутились в таком порядке: 3пр-2л-1л-1пр-3л-2пр, то после убиения всей живности на балконе вам нужно будет разворачивать их в порядке: 2пр-3л-1пр-1л-2л-3пр. 
 Ничего сложного тут нет, да и страшного тоже. Если вы крутанете не ту, то получите нехилый дот ядом. Но всего на 4 секунды, так что он не снесет вам и половины хп =) Так что метод тыка тут тоже работает, но запомнить и восстановить последовательность с первого раза - оно всегда приятно. 
 Да, и кстати. Порядок каждый раз разный. Т.е. новый поход в инст - новая комбинация =) 

 Часть 1. Балкон. 

 Когда босс убежит, решетки, что закрывали проход, откроются. Налево или направо вы пойдете - это неважно: балкон круговой. С одной стороны три статуи и три мастер элиты, что охраняют крутящий механизм, и с другой они же. Плюс орки с духами, что ходят туда сюда, а также реликвии на полу, что нужны для квеста. (так же на балконе вам встретятся три активные груды золота, заюзав которые, вы откроете деяние. 4-ый бриллиант, который нужен для завершения деяния, случайным образом дропается с мобов внутри сокровищницы). 
 Осторожно пульте мобов - вполне реально вытаскивать либо только ходоков (орки, духи), либо стража мастер-элитку. (Если схватите всех, будет больно. Но не поддавайтесь панике, и такую толпу реально завалить. ) Осторожно продвигайтесь вперед, вычищая всех. Когда на балконе никого не останется, нужно будет переходить к следующей части - вертеть статуи. 

 Примечание. 

 1. Не забывайте лутить все трупы, с них падают квест-итемы. В один заход все квесты закрыть все равно не получится, но в два - если повезет - реально. 

 2. Орки могут вешать на себя очень неприятный бафф: полное поглощение любого урона + при ударе вероятность отхила на определенную величину. Т.е. продолжая его избивать, когда он повесил на себя это, вы можете полностью восстановить ему здоровье. Чего, естественно, делать не стоит. Бафф висит где-то секунд 15, так что просто подождите, пока он спадет, и только потом продолжайте бить орка. 

 3. Мобы в сокровищнице (особенно мастер-элитки на балконе и босс Морхун) часто вешают ДоТы. Либо типа *Рана*, либо типа *Страх*. Дамаг от него... кхм. большой. Если не снимете с себя эту гадость (или ХМ/капитан не снимет ее с вас), то скорее всего умрете. Внимательно следите за собой и товарищами и предупреждайте их, если увидели на них этот ДоТ. 

 4. Мастер-элитки, с которыми вы будете сражаться, обладают интересной особенностью. Они циклически переходят из состояния *Телесное* в *Бесплотное* и обратно. Находятся в каждом они около 30 сек. В Телесной форме на них весит бафф на *90% смягчения к тактическому урону* (минсы, ХМ-ы, рунки отдыхают), а в Бесплотной - 90% смягчения к урону ближнего и дальнего боя. (отдыхают... почти все =)) 
 ВАЖНО. 
 Около крутящего колеса, после убийства мастер-элитки, вы сможете открыть сундук: в нем будет либо пурпурный осколок, либо желтый осколок. Цвет осколка зависит от того, в какой форме вы убили Стража. Если он умер в телесной, то получите желтый камень. В бесплотной - пурпурный. 
 Эти осколки нужны вам для убиения следующего босса, Морхуна. Он представляет собой более мерзкую и толстую версию Стражей, т.е. тоже имеет две формы. Если у вас пати рункиперов, то старайтесь добивать Стражей в Бесплотной форме и получать в сундуках пурпурные осколки. Если же нет - то ваш выбор - это умертвить стража в Телесной форме и получить желтый камень. 
 Эти камни можно (и нужно) юзать на боссе, Желтый осколок будет насильственно переводить его в Телесную форму, а пурпурный - в Бесплотную. 

 5. Также эти Стражи могут повесить на вас *глазик*. Его нельзя снять ни бутылками, ни скиллами. Дебаффает вас он на +50% к скорости применения умений, на -50% к скорости бега, или же прочая гадость. Так как сделать ничего нельзя, просто терпите =) 

 Часть 2. Статуи. 

 Собственно, после расчистки всего балкона, вам нужно развернуть статуи. Около каждого механизма, приводящего в движение статую, должно встать по человеку. Ну а дальше вы крутите эти колеса в нужном порядке, и наблюдаете за тем, как красиво преломляется свет Когда дело сделано, прыгайте вниз (обойти и спуститься по лестнице не выйдет, т.к. после правильной активации статуй решетки вновь закрываются.) Теперь ваша цель - проход за статуей, который ведет в комнатку с засевшим в ней первым боссом. Подождите, пока у вас не излечатся переломанные ноги, и двигайтесь туда. 

 Часть 3. Морхун. 

 Когда будете входить в комнату к Морхуну, делайте это все сразу, т.к. при активации босса дверь за вашей спиной закроется, отрезав замешкавшихся от остальных. Чего, конечно, не хотелось бы. 
 Сам босс несложный: аддов не зовет, особо мерзкими скиллами не отличается. Будет, правда, постоянно вешать на вас *глазики*, замедляя тем самым хил в два раза, и, что немаловажно, скиллы на снятие ран и страха. Так что тут лучше сразу выпить бутылку и снять с себя эту гадость, если на вас ее кинут. 
 А так... *Пилите Шура, пилите...* И не забывайте про палочки из сундучков, переводя босса в нужную вам форму, дабы ускорить процесс. 
 Когда Морхун будет повержен, вы можете открыть три сундучка с заслуженной наградой, но не расслабляйтесь, самое интересное еще впереди. 

 Часть 4. Скум и Ураут. 

