 Осторожно. 
 Когда вы будете переступать порожек, который отделяет вас от территории площадки, делайте это синхронно всей патей. Скорость захлопывания дверей поражает, а предстоящий бой будет сложнее битвы с Морхуном. 
 Когда вся пати окажется на площадке, то справа и слева от возвышающейся по центру статуи вы сможете лицезреть двух братьев троллей. Справа - Ураут, слева - Скум. Эту сладкую парочку надо всячески разводить, и разбегаться самим, когда они оказываются рядом друг с другом. Когда они вместе, они обладают каким-то страшным дпс-ом, шмаляют направо и налево жуткими аое атаками, поглощают часть урона и забирают входящий дамаг друг у друга и что-то еще, хотя, по сути, и перечисленного достаточно =) Плюс ко всему прочему взять измором их не получится: на вас (и на всю пати) ложится что-то типа *ауры болезнетворного грибка*, что постепенно кушает ваш максимум хп и мп. Сражаться с ними, когда тебя от силы хватит на один их удар - занятие малорадостное, тут никакой хил не поможет. 
 Собственно, это основное. Не затягивать бой, стараться разводить красавцев и вовремя убегать из-под аое, когда они сходятся. Будьте внимательны и все получится. 
 Когда с братцами будет покончено, у подножия статуи вас будет ждать сундук. Не забудьте забрать награду, хотя, на мой взгляд, 3 сундука у Морхуна и один у братьев - это как-то нелогично =)) Но ничего не поделать, а если захочется еще сундуков, то всегда можно сходить еще раз и снова зачистить всю сокровищницу. 

 Эпилог. 

 - Если вы умираете в сокровищнице, то вы возрождаетесь внутри нее, у входа. Если еще в процессе зачистка балкона, то вы можете присоединиться к своей пати. Если же уже имеет место быть бой с Морхуном или же с троллями - то нет. В первом случае закрыта дверь в комнату с Морхуном, во втором - новая решетка преграждает путь к лестнице. 

