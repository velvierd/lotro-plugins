 Вариант 1: 3 каерлуга. Чтобы открыть ворота к ним, необходимо разделиться. Один человек бежит к рычагу Р1, двое стоят у ворот. Один человек включает рычаг Р1 и ворота поднимаются. Внимание: этот человек не сможет вернуться к вам, посколько все другие проходы заперты. Решение: двое входят в ворота, третий снова выключает рычаг Р1. После этого он присоединяется к вам перед закрытыми воротами. Вы включаете рычаг за воротами и ворота снова открыты и ваш товарищ может пройти к вам. Дальше вы сражаетесь с мобами и лутаете сундук с них.

 Вариант 2: 12 каерлугов. Здесь вам нужно поднять мостик. Для этого вы снова разделяетесь. Один бежит к колесу К1 и рычагу Р2. Двое других стоят на краю моста. Третий включает рычаг и дергает колесо - мост появится на 5-10 секунд - двум другим нужно быстро перебежать на другую сторону. После этого мост пропадет. Третий возвращается к вам по ту сторону моста. Вы дергаете рычаг рядом с вами и мост появляется на 5-10 секунд снова - ваш третий товарищ должен успеть пробежать к вам. После этого вы сражаетесь с мобами и лутаете сундук с них.

 После того, как вы прошли либо первый либо второй варианты, вы возвращаетесь к спуску СП1. Все спускаются вниз и оказываются на самом нижнем уровне подземелья. Впереди находится комната с последним боссом инстанса - Каерлугом. Перед входом в его комнату находится реликвия для выполнения эпика - 2.8.4. 

 Битва с боссом - это необычная комната. В ней на потолке находятся слева и справа прессы. В дальнем конце у стены находятся сундуки (один с обычным лутом, второй на +15 монеты и третий -квестовый). Слева и справа от сундуков в углах находятся рычаги. Они управляют потолком - включая и отключая прессы слева и справа. Каждые 10-15к снятых у босса активируют скрипт - босс зеленеет и отползает лечиться в какой-нибудь угол. При этом работают прессы. Ваша задача - как можно быстрее добраться до рычагов и выключить тот, на чьей стороне находится босс. Это заставит его прервать лечение и выйти на битву. Внимание - старайтесь не попадать под прессы сами - они станят и больно бьют. В остальном битва довольно тривиальна - босс может также отбрасывать вас в стороны. Совет: с самого начала два человека бегут на рычаги слева и справа, третий - танк стоит по центру. Каерлуг имеет дурную привычку идти бить того, кто в центре. Имеются проблемы с контролем агро на нем. Даже если агро висит на вас, это не означает, что он не побежит расправляться с вашим хилером :) В целом, нужен крепкий танк/чемп и хороший хилер :)

 Вот собственно и все :) В инстансе есть несколько дидов: дид на цветки, дид на ящериц (т1 и т2, за т1 дают титул, а за т2 руну опыта), а также дид на боссов. Надеюсь, гайд будет полезным для тех, кто пойдет туда впервые и не любит разгадывать головоломки."

