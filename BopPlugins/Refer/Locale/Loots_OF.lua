 Опушка Фангорна - один из инстансов кластера "Изенгард", добавленный в обновлении U5. На опушке леса Фангорн орки Сарумана соорудили масштабную лесопилку, на которой денно и нощно валят могучие деревья для кузниц Изенгарда. Малому братству (рекомендуемый состав: танк, хилер, дамагер) предстоит нанести яростный удар по лесопилке и остановить вандализм, хотя бы ненадолго.

Встречающиеся мобы
Орк Изенгарда (Isengard Orc), слабый враг.
Урук Изенгарда, обычный враг.
Волчий всадник Изенгарда, сильный враг.
Тролль Изенгарда (Isengard Troll), могущественный враг. Тролль ваншотит ударом могучей дубины, так что вступать с ним в бой стоит только ради выполнения деяния "Дуболом" (умереть от удара тролля).
Мини-гайд

 Прямо перед вами орки безжалостно рубят гворна. Гворны - сильные воины, но устоять перед кучей орков с топорами они не в силах. Первого гворна вы спасти не успеете, но чтобы выполнить условия харда, всех остальных гворнов необходимо спасти.

 Первый этап инстанса представляет собой извилистый путь наверх, к лесопилке. Сплавка леса вниз, к Ортханку, осуществляется посредством длинной конструкции, которую усиленно патрулируют слуги Сарумана. По пути наверх вам несколько раз предстоит пересечь данное место и, что жизненно важно, не попадаться на глаза патрулям. Стычка с патрулем практически всегда заканчивается вайпом. Дожидайтесь крупного просвета между патрулями - и вперед.

 Четыре раза вы станете свидетелями того, как орки набрасываются на гворнов, пытаясь превратить их в щепки для костров Сарумана. Допустить подобной жестокости вы не можете, так что задача танка - сагрить всю кучу лесорубов на себя. Гворн неплохо помогает в битве, так что держитесь рядом с ним. Если боевой дух дерева значительно упал, менестрель может его восполнить.

 На опушке леса, рядом с огромной пилорамой, меж четырьмя гворнами расположился командующий орков. Ундурз (Undurz), могущественный враг. Особой сложности он не представляет, разве что каст особо мощной атаки по случайной цели рекомендуется прерывать. Во время битвы с Ундурзом время от времени появляются отряды лесорубов, которые нападают на ближайшее из четырех деревьев. Танк должен подвести босса к оркам и заагрить их всех. Дамагер спокойно разбирается с лесорубами, а благодарный гворн ему в этом помогает. Когда с аддами будет покончено, возвращайтесь на центр поляны и продолжайте крушить Ундурза до следующей волны аддов. Гворн, которому вы помогли, будет продолжать атаковать босса в довольно большом радиусе. Продолжайте процедуру, пока урук-командир не падет.

 Ундурз убит, но расслабляться рано. Гворны расступаются, и начинается более значимое событие: на поляну выходят три крупных энта. Всем известно, что энты - большие тугодумы, так что отличить вас от орков они не в силах. Энты начнут яросто топтать все, что движется, и ваша задача - сохранять себя в живых до финальной битвы. Самая действенная тактика - вернуться на холм, с которого вы спустились, и переждать мясорубку там. Новые орки появляются у подножия холма, и вас трогать не будут. Развернувшееся действо от вас никак не зависит, и сбросить его нельзя (оно прокручивается с начала лишь после смерти энтов).

 Итак, на арену выходят три здоровенных тролля. Энты, завидев противников посерьезнее орков, начинают бой с троллями один на один. Тут-то и вступает в дело наше храброе братство. Для прохождения инстанса достаточно, чтобы выжил хотя бы один энт, но героев Средиземья привлекают тяжелые испытания, так что спасать энтов на хард нужно всех. Самая действенная тактика сейчас такова: танк забирает на себя всех мелких прихвостней и кайтит их по поляне, в то время как дамагер и хилер начинают помогать энту, который пострадал от схватки с орками больше всего (т.е. с наименьшим количеством боевого духа). Менестрель переключается в "Боевую речь", хранитель рун настраивается на бой - и в атаку. Убить первого тролля нужно как можно быстрее. Дальше станет легче - энт, оставшийся без соперника, отправится помогать своему соседу, а задача братства - помочь третьему энту. Убив еще одного тролля, остается только помочь троим энтам добить последнего тролля и насладиться победой. Если танк не падет под ударами орков во время кайта, и если хилер хоть изредка будет его подлечивать, то герои Средиземья уверенно одержат победу. Также будьте аккуратны с АоЕ-уроном - энты все-таки настроены к вам враждебно и тоже могут получать от вас урон.

