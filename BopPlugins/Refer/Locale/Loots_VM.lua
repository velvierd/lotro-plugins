 Великий Могильник (Отронгрот) - самый первый инстанс, который открывается игроку. Когда-то он являлся одной довольно крупной инстансной локацией, теперь же Великий Могильник состоит из трех относительно небольших частей. Великий Могильник - идеальное место для новичка попробовать себя в командной игре.
 
{---------Великий Могильник: Лабиринт/Great Barrow: The Maze---------}
 Уровень: 20-75.
 Размер группы: 6.
 Уровней сложности: 1 (хард-мод присутствует).
 Регион: Земли Бри, Южные Могильники.

Встречающиеся мобы
Древнее могильное умертвие (Ancient Barrow-wight), элита.
Злобное курганное умертвие (Grim Tomb-wight), сильный враг.
Отрубленная рука (Crawling Arm), слабое существо.
Прожорливая могильная склопендра (Consuming Barrow-crawler), обычный враг.
Жирная могильная склопендра (Bloated Barrow-crawler), слабое существо. При столкновении с игроком взрывается, выпуская облако кислоты. Игроки, стоящие в облаке, получают постепенный урон кислотой.
Огромная могильная склопендра (Devouring Barrow-crawler), элита.
Мини-гайд

 Данный инстанс проходится довольно ровно. Серьезных боев предстоит два: с могильным глашатаем в комнате на середине пути и с умертвиями Гаэртелем и Гаэрдринг, братом и сестрой, в конце.

Могильный глашатай (Barrow-wight Caller), сильная элита. В самом начале боя призывает двух злобных курганных умертвий. Потеряв большую часть боевого духа, глашатай временно становится неуязвимым, чтобы призвать двух огромных могильных склопендр. Они, в свою очередь, постоянно призывают жирных могильных склопендр, которые взрываются и наносят урон кислотой.

 Стоит помнить, что наверху лестницы, с которой вы спустились к глашатаю, босс сбрасывается. Чтобы облегчить предстоящий бой, можно выманить и перебить всех бродящих по комнате умертвий. Если при этом сагрится босс, вы легко сбросите его на верхушке лестницы.

Гаэрдринг (Gaerthel) и Гаэртель (Gaerdring), сильная элита. Бой начинается с небольшой скриптовой сценки с разговором. Первой активируется Гаэрдринг, стоящая слева. Она постоянно призывает умертвий и отрубленные руки, так что ваша задача - как можно быстрее одолеть Гаэрдринг, не давая при этом аддам загрызть вашего хилера. Гаэртель вступает в бой сразу после смерти сестры.

Хард-мод. На 75 уровне в данном инстансе доступен хард-мод. Условия харда: убить 20 отрубленных рук, не убивая прочих умертвий, до убийства Гаэрдринг. Так как после убийства 20 рук активируется Гаэртель, а будучи вдвоем, брат и сестра вешают на себя щиты, которые превращают входящий дамаг в отхил, либо возвращают урон, рекомендуется поступить следующим образом. Когда активируется Гаэртель, начинают появляться адды с довольно мощным неснимаемым пороком, повышающим урон. Их водит по кругу танк (лучше всего - рыцарь, защитник), в то время как остальные члены братства доводят Гаэртель до 5000 боевого духа и попутно убивают 19 рук. Потом добивается последняя рука, следом активируется Гаэртель. Осталось только быстро добить Гаэрдринг (благо, боевого духа вы ей оставили немного), и заняться ее братом.

 Если у вас не получилось быстро убить Гаэрдринг и брат повесил на нее щиток на отхил, просто не бейте Гаэрдринг по время действия щита и займитесь Гаэртелем. Щит Гаэртеля имеет шанс возвратить вам часть урона, но это сейчас меньшее из зол, так что убивайте его без промедления. Потом уже можно будет заняться Гаэрдринг, просто дожидаясь, пока спадет ее щит на отхил. Данный хард-мод считается самым сложным в Великом Могильнике, но при правильной координации действий у вас все получится.

{---------Великий Могильник: Тадур / Great Barrow: Thadur---------} 
Уровень: 22-75.
 Размер группы: 6.
 Уровней сложности: 1 (хард-мод присутствует).
 Регион: Земли Бри, Южные Могильники.

Встречающиеся мобы
Неупокоенный лучник (Barrow-wight Bowman), элита.
Неупокоенный мастер-лучник (Barrow-wight Bowlord), элита.
Глашатай битвы (Gaunt Battle-caller), элита.
Холодное курганное умертвие (Chilled Barrow-wight), сильный враг.
Могильная гончая (Barrow-hound), обычный враг.
Холодный могильный огонек (Chilled Barrow-light), сильный враг. Не нападает сам, появляется в предфинальной стадии инстанса.
Мини-гайд

 Инстанс представляет собой небольшое крыло с парочкой закоулков. Ваша задача - зачистить все крыло от рядовых мобов, и тогда Тадур обратит на вас внимание. Он предложит братству поочередно разобраться с двумя, тремя и четырьмя могильными огоньками, причем сделать это нужно быстро. Огоньки появляются случайным образом на всей территории крыла, так что имеет смысл разойтись пошире. Главное требование: быстро сагрить огоньки, а уничтожать их можно сколь угодно долго.

 Как только вы расправитесь с огоньками, будет открыта малая северная комната с элитными мобами. Разобравшись с ними, Тадур наконец позволит вам пройти к нему в большую северную комнату.

Тадур (Thadur the Ravager), могущественный враг. Трижды бой с ним будет останаваливаться - на отметках 85%, 50% и 25% боевого духа, и всякий раз Тадур будет призывать волны аддов. Пока адды живы, Тадур поглощает весь входящий урон, так что сперва нужно разобраться с аддами. Тадур обладает несколькими неприятными способностями: он частенько высасывает энергию, а также накладывает на игроков серьезные болезни. Одна из болезней понижает стойкость, вторая - понижает восстановление боевого духа и энергии в бою, а также включает 20-секундный счетчик, по истечению которого вы получаете 15-секундный стан и теряете практически всю энергию. Банки против болезней сослужат неплохую помощь.

Хард-мод. На 75 уровне в данном инстансе доступен хард-мод. Условия харда: во время каждой из трех волн аддов зажечь по жаровне. Жаровня становится активна, когда Тадур призывает очередную порцию аддов, причем во время второй волны ее активация повышает урон Тадуру и мобам, а во время третьей - позволяет Тадуру и мобам отражать часть урона. Самая действенная тактика - активировать жаровню только тогда, когда останется в живых последний моб пачки, причем с небольшим количеством боевого духа. Зажигаем жаровню, добиваем адда и продолжаем биться с Тадуром. Так вы сведете к минимуму негативное действие от жаровен.

{---------Великий Могильник: Самброг / Great Barrow: Sambrog---------}
Уровень: 24-75.
 Размер группы: 6.
 Уровней сложности: 1 (хард-мод присутствует).
 Регион: Земли Бри, Южные Могильники.

Встречающиеся мобы
Неупокоенный лучник (Barrow-wight Bowman), элита.
Древнее могильное умертвие (Ancient Barrow-wight), элита.
Глашатай битвы (Gaunt Battle-caller), элита.
Проклятая тень (Accursed Fell-spirit ), сильный враг.
Могильная гончая (Barrow-hound), обычный враг.
Мини-гайд

 В самом первом помещении можно легко потерять многих членов братства, если неподготовленно ринуться вперед. На первый взгляд, помещение пусто, однако умертвия начнут появляться из разных концов зала, как только кто-нибудь из игроков достигнет закрытой двери, ведущей дальше. Залог успеха - держаться вместе и не позволять отдельным мобам грызть где-то вдалеке игроков, оставшихся в одиночестве. Для низкоуровневого братства разделение здесь смерти подобно.

 Остальные коридоры и комнаты проходятся без запинок, и в последнем зале предстоит финальная битва с Самброгом.

Самброг (Sambrog), могущественный враг. Самброг вешает на игроков ауру +2 к ужасу, а также имеет пассивные способности, наносящие окружающим урон тьмой и кислотой. В течение боя он призывает на помощь теней, - рекомендуется с ними расправляться, так как они пытаются отлечивать босса. Иногда Самброг выбрасывает фиолетовое облако с довольно сильным ДОТом, так что старайтесь не стоять в его пределах.

Хард-мод. На 75 уровне в данном инстансе доступен хард-мод. Условия харда: перед битвой с Самброгом активировать гробницу, которая вешает на босса нарастающий порок на урон и увеличивает количество призываемых мобов. Данный хард считается самым легким среди трех инстансов Великого Могильника. Активируем гробницу и быстро зергаем босса, не забывая прерывать хилящих теней.

 PS. Когда-то с Самброга падал обменный предмет на трофей в дом. Теперь же трофей "Шлем Самброга" (Sambrog's Helm) можно приобрести в лагере схваток.

 В Великом могильнике падают значки и медальоны, за которые можно приобрести один из трех неплохих сетов на 25 уровень - легкий, средний или тяжелый. Это самые первые сеты в игре.

