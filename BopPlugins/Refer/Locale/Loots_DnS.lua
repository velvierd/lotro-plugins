Описание инстанса
Инстанс на малое братство.
Как водится, имеет два уровня сложности, причём Второй - с испытанием.

Сам гайд

Первая комната: Вначале, мы поднимаемся наверх, где видим огромный стальной ящик, помеченный Белой Дланью. Рядом с ним невинно болтают орки, обсуждая поимку пленника. Через некоторое время тролль просыпается и охваченный яростью выбивает дверь изнутри. После чего радостно сообщает нам, что "Даргнакх на свободе!" и убегает в соседнюю комнату. Братство бежит туда (только поверху) и видит три баллисты, нацеленные вниз (каждая на свою точку). Рядом со входом лежат болты, для них. Двое (то есть хилер и дамагер) берут их и несут к баллистам, дабы зарядить их. А потом поджидают момента, когда тролль подбежит к нужном месту, и стреляют из нужного орудия (Босс постоянно сражается с орками внизу, поэтому дамага от него не будет). На Т2 есть особенность в этой комнате, появляются три орка, которые моментально воскрешаются, оглушают свою цель и наносят большой урон(800-900 хп). Задача танка - сагрить их всех на себя и бегать большими кругами, постоянно кайтя и не отвлекаясь ни на что больше. Хилер иногда его подлечивает, когда танк пробегает близко. В общем, как только выпустите 3 или 4 болта (точно не помню), босс пробьет стену и пройдет в следующую комнату. Первая комната пройдена!

Вторая комната: Комната с бочками набитыми порохом. Там уже придется попотеть. Ибо нам нужно будет спуститься уже вниз. Внизу происходит конкретный замес. Орки не отступают в попытке схватить это Чудовище, а он им не сдается, но как всегда, и никто из них не любит Свободные Народы. По комнате разбросаны бочки с порохом. Задача танка - снова заагрить на себя всех и кайтить. Хилер его постоянно лечит, или же помогает дамагеру. Задача дамагера - брать факел, который хаотично появляется на стенах по всей комнате и подносить его к бочкам, которые находятся рядом с троллем, тем самым отнимая у него по 100к здоровья за один взрыв (Советую сразу бежать из огня). Так же - 3 или 5 факелов и босс в ярости выйдет уже наружу, чтобы подышать свежим воздухом.
 ВНИМАНИЕ! На Т2 под ноги Даргнакху лучше не попадаться. Танк, может, и выдержит, а вот тряпочки сложатся сразу.
 Поздравляю, вторую комнату вы прошли!

Улица: Когда мы бежим за Даргнакхом с криками: "Стой собака, я тебя еще не дорезал!", перед нами предстает картина маслом. А именно - Тролля скрутили колдуны, во главе которых стоит Хува, самый мощный из них. Шестеро мелких же сидят и держат в узде, необузданное чудовище. Снова неприятности у нас, но ничего не поделаешь, ведь половину уже прошли. Советую оббафаться по полной, ибо бой будет длинным, и запастись как можно большим количеством банок от страха и ран (раны будут не так важны как страх; будет хорошо, если в группе присутствует рыцарь или Хранитель Мудрости, ибо банок всегда не хватает). Сначала мы деремся с Хувой, остальные сидят спокойно и не дергаются. У Хувы есть скилл, который нужно будет быстро прервать в течении 4-5 секунд, или все получите неслабый урон огнем по площади, определить каст можно по оранжевому кругу, под ним и странными пасами его пальцев, не успели прервать - значит отбегайте. 

 Колдун еще вешает оглушающий страх на 10 секунд и страх с иконкой глаза, наносящий урон по 2000 каждые 2 секунды в течении полуминуты. Чуть позже, к своему предводителю, отрываясь от поддержки заклинания "усыпления", присоединяются два колдуна. Они висят на его лечении. И их надо прерывать, а так же ни в коем случае не подпускать к хилу. Так будет три раза, пока мы не убьем всех мелких и не прибьем Хуву. 

