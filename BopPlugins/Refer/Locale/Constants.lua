_G.Help = [[Плагин «Справочная» вер. ]].._G.Version..[[.

Плагин представляет собой информационную базу, содержащую данные о предметах, добываемых в инстанасах, сетах, о прохождении инстансов и о деяниях на добродетели.

«Показывать прохождение».
Если включить опцию «Показывать прохождение», то при выборе конечного узла в дереве(название инстанса, класса, босса в интансе) в правом окне будет отображаться не список предметов, а текст с описанием прохождения или стоимость стоимость добываемых сетов, обмениваемых предметов.

«Линкование в чат и примерка».
Справа каждого предмета есть серое поле, обозначающий доступность данных функций. Если на нём есть зелёная стрелка – функции линкования доступны, нет – не доступны. Если есть зелёная стрелка, то:
  1. Клик ЛКМ по названию предмета откроет внутриигровое зафиксированное описание предмета (максимум одновременно открытых описаний = 4).
  2. Ctrl + клик ЛКМ по названию предмета откроет примерочную с данным предметом, если, конечно, он примеряемый.
  3. Клик ЛКМ по зелёной стрелке откроет меню с перечнем каналов, куда можно отправить ссылку на предмет. Ссылка отправляется при клике ЛКМ по стрелке в соответствующем канале.

«Фильтр».
Фильтр предназначен для отбора только тех предметов, характеристики которых соответствуют выбранным. Включение/откючение фильтра осуществляется с помощью установки/снятия отметки «Использовать фильтр»

«Добродетели».
Окно открывается/скрывается нажатием по соответствующей иконке на панели «Спр». Окно содержит информацию для просмотра деяний необходимых для развития той или иной добродетели. Так что можно посмотреть бонусы, даваемые добродетелью на разных уровнях, для этого надо:
  1. Выбрать любую зону выполнения деяний в списке добродетели.
  2. Ввести желаемый уровень в поле «Уровень добродетели».
  3. Нажать кнопку «->».
По умолчанию бонусы отображаются для максимального уровня добродетели.

«Линкование в чат деяния на добродетель».
Для того, чтобы вывести в чат игры описание деяния(линкануть), достаточно щёлкнуть ЛКМ по заголовку деяния и в выпавшем меню нажать на стрелку в строке с соответствующим чатом. 

«Изменение порядка сортировки добродетелей».
В окне «Справочная: Добродетели» вверху слева есть поле «Сортировать по локациям». При установки в нём галки дерево добродетелей перестроится по принципу: Регион-Локация, - а информация справа будет отображать все деяния на добродетели для выбранной слева локации. Снятие галки вернёт принцип построения дерева к первоначальному виду.

«Отметка о выполнении деяния».
В окне «Справочная: Добродетели» в заголовке описания каждого деяния в левом верхнем углу есть поле для отметки выполненности добродетели. Ставится она только вручную. Согласно этим отметкам будет отображаться прогресс развития добродетели (если уставновлена опция «Показывать прогресс выполнения деяний»)

«Фиксирование описания деяния в отдельном окне».
В окне «Справочная: Добродетели» клик ПКМ по заголовку выведет описание деяния в отдельное окно.

«Гардероб»
Гардероб представляет собой сборник лучших костюмов, расположенных в разделе форума «Дефиле».
При выборе костюма справа появится подробное описание с изображением костюма. Справа и слева костюма расположены ссылки на элементы костюма (можно примерть на себя с помощью нажатия Ctrl+ЛКМ).
Под изображением расположено подробное описание костюма.

«Линкование костюма в чат».
Понравившийся костюм можно линкануть в чат, для этого надо нажать ЛКМ по названию костюма в описании справа и в выпавшем меню выбрать желаемый канал чата.

«Прочее»
В разделе «Прочее» собрана информация по комбо-приёмам братства с возможностью расписания приёма по членам группы, эмоциям  и различным командам с возможностью их запуска. 

«Линкование костюма в чат».
Понравившийся костюм можно линкануть в чат, для этого надо нажать ЛКМ по названию костюма в описании справа и в выпавшем меню выбрать желаемый канал чата..

«Настройка».
Открывает/скрывает окно настроек. Доступные опции:
  - «Кнопка «Предметы» на панели быстрого запуска» - вкл./выкл. отображение на панели быстрого запуска кнопки, для вызова окна «Справочная: Предметы».
  - «Кнопка «Добродетели» на панели быстрого запуска» - вкл./выкл. отображение на панели быстрого запуска кнопки, для вызова окна «Справочная: Добродетели».
  - «Кнопка «Лупа» на панели быстрого запуска» - вкл./выкл. отображение на панели быстрого запуска кнопки, для вызова окна «Справочная: Лупа».
  - «Кнопка «Помощь» на панели быстрого запуска» - вкл./выкл. отображение на панели быстрого запуска кнопки, для вызова окна «Помощь».
  - «Кнопка «Гардероб» на панели быстрого запуска» - вкл./выкл. отображение на панели быстрого запуска кнопки, для вызова окна «Справочная: Гардероб».
  - «Кнопка «Прочее» на панели быстрого запуска» - вкл./выкл. отображение на панели быстрого запуска кнопки, для вызова окна «Справочная: Прочее».
  - «Подсказки на панели быстрого запуска» - вкл./выкл. отображение подсказок, появляющихся при наведении курсора мыши на кнопки на панели быстрого запуска.
  - «Показывать прогресс выполнения деяний» - вкл./выкл. отображение полосок степени развития добродетели в окне «Справочная: Добродетели».

