Рекомендованный состав 
Составы могут быть разными, но наиболее подходящий выглядит так:
Танк – страж, защитник.
Офф-танк – защитник, воитель, страж.
Хиллер – менестрель, хранитель рун в хиле.
Офф-хилер – рыцарь.
ДД – воитель, охотник, взломщик…
Контроль или ДД – хранитель мудрости, взломщик…
Я вижу пак так: страж, вой, хант, рыц, мен, хм (прим. редактора).


Треш
После того, как все зашли и пробаффались, вам встретится первая группа из 6 мобов по 19к-30к боевого духа. Сначала убиваем орков-шаманов, потом орков-бойцов.

 Двигаемся дальше по мосту и встречаем следующую группу - в ней первым убиваем осквернителя.

 Продолжаем зачистку, пока не увидим гигантского спящего тролля со 176к боевого духа. Тут есть выбор: либо убиваем его, либо уходим в правый коридор сразу к боссу.


Первый босс - Фрушкул 
Простой босс с 369к боевого духа и охраной, состоящей из орка-шамана и орка-бойца.

 Пока все дамагеры разносят сперва шамана, а затем орка, танк отводит босса в угол и там быстренько набирает максимальное агро.
Внимание, костёр в центре может мешать работе по цели хилерам и дальникам (цель вне зоны видимости), обходите его.

Вещи за которыми надо следить:
Гоблины-доносчики (~7.5к боевого духа)
 После того, как Фрушкул скажет «Эй, ты, коротышка дохлый! Зови парней на помощь!», у ворот появится гоблин и побежит к выходу. Дальники и контролеры должны убить его максимально быстро. Если же он убежит, то приведет еще кучку орков.

 За весь бой гоблин появится 3 раза. Причем последний доносчик появится вместе с группой орков.
Адды
 3-4 раза в течение боя появятся группы аддов, состоящие из нескольких шаманов и бойцов. Убиваются быстро в том же порядке, что и раньше – шаманы первыми.
АОЕ-атаки
 Периодически Фрушкул будет говорить: «Я разорву тебя на кусочки!». В это время все, кто находится в мили-зоне босса, должны от него отойти. АОЕ-атака тройная.
Пороки
 На боссе постоянно будут появляться пороки, которые увеличивают его урон, их нужно сразу же снимать. Особенно это важно перед его АОЕ-атакой.


Пауки-сетеплёты 
Следующим вам встретится паук-сетеплёт со 115к боевого духа. Дайте танку его наагрить и спокойно убивайте.

 Примерно на 50% боевого духа сетеплёта приползут 3-4 паука-охотника по 35к каждый. Их забирает также танк. Добиваем гигантского паука, а уже потом раскладываем маленьких и двигаемся дальше.

 Здесь вешается неснимаемый яд средней силы, убедитесь, что ваш хилер не слакает.

 Поднимаемся налево и видим сетеплёта и двух пауков-охотников. Снова предоставляем танку свободу агро, убиваем мелких пауков первыми. Та же система - на 50% здоровья паука-сетеплёта наползут 2 паука-охотника и 3 ядовитых паучонка (15к). Ядовитые пауки вешают неснимаемый яд по 5-7к урона каждые 2 секунды, поэтому лучше всего их контролить и БЫСТРО УБИВАТЬ. А потом раскладывать всех остальных в известном порядке.

 Дальше мы встретим худенького сетеплёта с 75к боевого духа, но в начале боя от входа приползёт ещё один. Система прежняя - танк агрит, остальные дамажат.

 Будет две волны аддов из 2 пауков-охотников и 2 ядовитых паучат. Убиваем паучат, пока они не успели наложить свой дот.


Финальный босс - Гуртул 
Гуртул - паучиха с 569к боевого духа, главная негодяйка, отравляющая корни Фангорна.

 Есть несколько вариантов прохождения, но всё зависит от состава братства.

Вариант 1:
 Танк ловит аддов, офф-танк держит босса (защитник или воитель в ореоле славы). В этом варианте вам понадобится действительно хороший хилер.
Офф-танк отводит босса и пытается его максимально наагрить (если это воитель, то дайте ему секунд десять; при этом охотники должны быть в стойке выносливости).
Фронтальное АОЕ - паучиха бьет АОЕ-атакой перед собой; способна сваншотить тряпок. Офф-танк держит её лицом к стене возле ближнего ко входу правого корня (так ему удобнее снимать яд). ВНИМАНИЕ! Офф-танк не играет комбо!
Танк держит аддов - ядовитых паучат, спускающихся на паутинках с потолка сперва у входа, потом у водопада, а затем в центре водопада, и заагривает на себя. Если кайтить, то они скорее всего сорвутся на менестреля. Лучше не кайтить, а держать у водопада, переключаясь постоянно с уцели на цель; после убийства приходит новый паучок. Также на танке будет висеть дот на 5-7к урона в 2 секунды, так что не забываем его лечить, особенно это касается рыцаря.
Корни и яды. Периодически Гуртул будет плеваться неснимаемым стакающимся ядом - дотом по 500 урона. Каждые 30 секунд будет всплывать надпись «Один из корней пытается вас излечить». Всего их 6, по 3 с двух сторон комнаты. Желательно, чтобы какой-нибудь игрок с голосом озвучивал это. Когда появляется надпись, тот, у кого больше всего дотов и меньше всего боевого духа, бежит в зелёный кружок у нужного корня. Это снимет все эффекты.

Отличия Т1 и Т2: на Т1 корень снимает дот со всех, кто зашёл в круг, на Т2 - только с одного.

Условия харда - "Защитить все корни леса". Это значит - не дать паучкам отравить корни. То есть убить Гуртул с первого раза и не дать паучкам укусить корни при появлении.

ВНИМАНИЕ! Если все корни будут отравлены, это конец боя.

 Когда у Гуртул останется примерно 230к, адды начнут появляться в центре комнаты. Убедитесь, что ваш танк забрал их всех. Продолжайте дамажить босса, а танк в это время будет бегать от 6 маленьких поклонников.

Оптимальное комбо - "Чистота помыслов",  (3 зелёных, 2 синих). И "Сумятица в рядах" на боссе в начале боя -  (четыре жёлтых). ВНИМАНИЕ! Танк, держащий паучиху, не играет комбо!

Вариант 2:
 Танкующий страж бастионит воителя/охотника/хранителя рун/любого, кто может просто забрать на себя паучка. Дот по 7к уходит в поглощение, проблем с отлечиванием нет.

Вариант 3:
 Если танк в братстве один, пусть держит и Гуртул, и забирает аддов, лечите его полностью (желательно иметь двух менестрелей, один из которых вполне может почти весь инстанс быть в боевой речи). Если контролеры пряморукие, часть аддов можно весь бой держать в контроле
 
